def setup_logging(resultDir):
    """Setup logging configuration with timestamped log file.
    Args:
        resultDir (str): Path to the result directory (SR_out/ subdirectory is created here)
    Returns:
        logger (logging.Logger): Logger object
    """

    log_dir = f"{resultDir}SR_out{SEP}"
    if os.path.exists(log_dir):
        shutil.rmtree(log_dir)
    os.makedirs(log_dir)

    timestamp = datetime.datetime.now().strftime("%Y%m%d_%H%M%S")
    log_filepath = f"{log_dir}SR_logs_{timestamp}.log"

    logging.basicConfig(
        level=logging.INFO,
        format='%(asctime)s - %(message)s',
        handlers=[
            logging.FileHandler(log_filepath),
            logging.StreamHandler()
        ]
    )

    logger = logging.getLogger(__name__)
    logger.info(f"Logging initialized. Log filepath: {log_filepath}")
    return logger


