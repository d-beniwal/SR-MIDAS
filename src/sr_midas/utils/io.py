def read_hkls_csv(hkls_csv_path):
    """Read the hkls.csv file generated by MIDAS.
    Args:
        hkls_csv_path (str): path to the hkls.csv file
    Returns:
        df_hkls (pandas dataframe): dataframe with hkls data
    """

    df_hkls = pd.read_csv(hkls_csv_path, sep=' ')
    return df_hkls


